#0 to 50 in increments of 1
onetofifty = 1
for onetofifty in range(0,51,1):
    print(onetofifty)
    onetofifty += 1

#50 to 1 in decrements of 1
fiftytoone = 50
while fiftytoone > -1:
    print(fiftytoone)
    fiftytoone -= 1

#30 to 50 in increments of 1
thirtytofifty = 1
for thirtytofifty in range(30,51,1):
    print(thirtytofifty)
    thirtytofifty += 1

#50 to 10 in decrements of 2
fiftytoten = 50
while fiftytoten > 8:
    print(fiftytoten)
    fiftytoten -= 2

#100 to 200 in increments of 5
hundredto2 = 5
