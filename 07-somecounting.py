#0 to 50 in increments of 1
onetofifty = 1
for onetofifty in range(1,51,1):
    print(onetofifty)
    onetofifty += 1
# Variable set to 1.
# For loop to loop a specific number of times, using range + parameters to set a starting, ending, and increment value.
# First parameter set to 1 to start at 1. Second parameter set to 51 so it would end at 50. Third parameter to set increment value to 1.
# "+=" operator to set the increment value by 1.

#50 to 1 in decrements of 1
fiftytoone = 50
while fiftytoone > 0:
    print(fiftytoone)
    fiftytoone -= 1
# Variable set to 50 so it would be the starting value.
# While loop to execute until the value is greater than 0 (it would stop at 1).
# "-=" operator to set the decrement value by 1.

