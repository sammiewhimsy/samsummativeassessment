#france
FranceParis = (input("1. What is the capital of France?\nAnswer: "))
FRanswer = "Paris"
while FranceParis != FRanswer:
    if (FranceParis == FRanswer) or (FranceParis.lower() == FRanswer.lower()) or FranceParis.upper() == FRanswer.upper():
        break
    else:
        print("Incorrect.")
        FranceParis = (input("1. What is the capital of France?\nAnswer: "))
print("Correct.")

